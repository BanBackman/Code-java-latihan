public class Nilai {
    public static void main(String [] args){ 
        
        //Inisiasi variabel tanpa nilai
        int a;

        //Inisiasi variabel dengan langsung memberi nilai
        int b=12;

        System.out.println("nilai dari variabel b="+12);

        //memberi nilai variabel a dan mengganti nilai dari variabel b
        a = 10;
        b = 20;

        System.out.println("Nilai dari variabel a="+a+" dan b="+b);
    }
}